#include "libft.h"

void	ft_putnbr(int n)
{
	long long	nb;

	nb = n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb >= 0)
	{
		if (nb >= 10)
		{
			ft_putnbr(nb / 10);
			nb = nb % 10;
		}
		nb = nb + 48;
		write(1, &nb, 1);
	}
}
