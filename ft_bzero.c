#include "libft.h"

void ft_bzero(void *b, size_t n)
{
if (n > 0) 
{
    size_t	        i;
	unsigned char	*ptr;

	i = 0;
	ptr = b;
	while (i < n)
		ptr[i++] = 0;
}
}
