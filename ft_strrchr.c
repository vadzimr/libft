#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int len;

	if ((s[0] == '\0' && c == '\0') || s[0] == '\0')
		return (NULL);
	len = ft_strlen(s);
	while (len >= 0)
	{
		if (s[len] == (unsigned char)c)
			return ((char *)&s[len]);
		len--;
	}
	return (NULL);
}
